package Compiler;

public class BPLScannerException extends Exception {
	public BPLScannerException() { 
		super(); 
	}

	public BPLScannerException(String message) { 
		super(message); 
	}

	public BPLScannerException(String message, Throwable cause) { 
		super(message, cause); 
	}

	public BPLScannerException(Throwable cause) { 
		super(cause); 
	}
}